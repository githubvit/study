# 将 标准像素图 QStyle.SP_ ... 转为 图标 QIcon 
    # 使用 app.style().standIcon(QStyle.SP_ ...) 返回的是PySide2.QtGui.QIcon，

from PySide2.QtWidgets import QApplication,QMainWindow,QWidget,QPushButton,QLabel,QTableWidget,QListView,QStyle
from PySide2.QtCore import Qt,Slot
from PySide2.QtGui import QIcon,QStyleHints,QPixmap,QPalette,QColor
# QPixmap 像素图控件是用来处理图像的控件之一。
# 它用于将优化后的图像显示在屏幕上。
# 在我们的代码示例中，我们将使用QPixmap 控件在程序窗口上显示图像。

import os,sys

app=QApplication([])
wd=QWidget()
wd.setWindowTitle('qt图标')
wd.setGeometry(1000,400,500,500)

pmp=app.style().SP_DirClosedIcon
print(pmp)#PySide2.QtWidgets.QStyle.StandardPixmap.SP_DirClosedIcon
ic=QStyle.SP_TitleBarCloseButton

print(ic)



btn1=QPushButton(wd)
btn1.setText('图标1')
btn2=QPushButton(wd)
btn2.setText('图标2')
btn3=QPushButton(wd)
btn3.setText('图标2')


btn1.setGeometry(100,50,100,30)
btn2.setGeometry(100,100,100,30)
btn3.setGeometry(100,150,100,30)


lq=app.style().standardIcon(app.style().SP_TitleBarCloseButton)


lq1=app.style().standardIcon(QStyle.SP_MediaPlay)
lq2=app.style().standardIcon(pmp)
# lq1=QStyle.standardIcon(ic)#报错 不能用QStyle.standardIcon()，必须用app.style().standardIcon(ic)
# print(lq,lq1)
btn1.setIcon(lq)
